export const getLivePulse = (destinations) => {
    return destinations.map(dest => {
        // Random fluctuation
        const crowdFluctuation = Math.floor(Math.random() * 31) - 15;
        const crowdLevel = Math.max(0, Math.min(100, dest.baseCrowd + crowdFluctuation));

        // Comfort score
        const comfortScore = Math.max(1, Math.min(5, Math.ceil(5.5 - (crowdLevel / 20))));

        let crowdStatus = "Low";
        if (crowdLevel > 65) crowdStatus = "High";
        else if (crowdLevel > 35) crowdStatus = "Medium";

        return {
            ...dest,
            currentCrowd: crowdLevel,
            crowdStatus,
            comfortScore,
            lastUpdated: new Date().toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' })
        };
    });
};
